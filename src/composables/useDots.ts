  function getPattern(dotSymbol: DotSymbols): number[][] {
    if (!(dotSymbol in digitPatterns)) {
      throw new Error(`Invalid dot symbol: ${dotSymbol}`)
    }
    return digitPatterns[dotSymbol]!
  }

